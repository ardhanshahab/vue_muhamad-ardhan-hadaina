export const penjumlahan = (a, b) => {
    return a + b;
};

export const pengurangan = (a, b) => {
    return a - b;
};

export const perkalian = (a, b) => {
    return a * b;
};

export const pembagian = (a, b) => {
    return a / b;
};

export const rataRata = (a, b, c, d, bagi) => {
    return a + b + c + d / bagi;
};

export const sisaBagi = (sisasatu, sisadua) => {
    return sisasatu % sisadua;
};

export const luasPersegi = (a, b) => {
    return a * b;
};

export const luasLingkaran = (phi, b) => {
    return phi * Math.pow(b, 2);
};

export const luasKubus = (stiga, sdua, enam) => {
    return enam * sdua * stiga;
};

export const volumeKubus = (p, l, t) => {
    return p * l * t;
};

